import { db } from "./firebase";
import { doc, setDoc, updateDoc, getDoc, deleteDoc, collection } from "firebase/firestore";

/** ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô‡πÉ‡∏´‡∏°‡πà */
export const createClassroom = async (uid, subjectCode, subjectName, photoURL, roomName) => {
  try {
    const newClassroomRef = doc(collection(db, "classroom")); // üîπ ‡∏™‡∏£‡πâ‡∏≤‡∏á reference ‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô‡πÉ‡∏´‡∏°‡πà
    const cid = newClassroomRef.id;

    const classroomData = {
      owner: uid, // üîπ ‡∏£‡∏∞‡∏ö‡∏∏‡πÄ‡∏à‡πâ‡∏≤‡∏Ç‡∏≠‡∏á‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
      info: {
        code: subjectCode,
        name: subjectName,
        photo: photoURL || "",
        room: roomName,
      },
    };

    await setDoc(newClassroomRef, classroomData); // üîπ ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏á Firestore

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤ `users/{uid}` ‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
    const userRef = doc(db, "users", uid);
    const userDoc = await getDoc(userRef);

    if (userDoc.exists()) {
      // ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ user ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
      await updateDoc(userRef, {
        [`classroom.${cid}`]: { status: 1 },
      });
    } else {
      // ‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ ‡πÉ‡∏´‡πâ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£‡πÉ‡∏´‡∏°‡πà
      await setDoc(userRef, {
        name: "‡πÑ‡∏°‡πà‡∏ó‡∏£‡∏≤‡∏ö‡∏ä‡∏∑‡πà‡∏≠",
        email: "",
        photo: "",
        classroom: {
          [cid]: { status: 1 },
        },
      });
    }

    return cid;
  } catch (error) {
    console.error("üî• Error creating classroom:", error);
    throw error;
  }
};

/** ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô */
export const updateClassroom = async (cid, updatedData) => {
  try {
    const classroomRef = doc(db, "classroom", cid);
    await updateDoc(classroomRef, { info: updatedData });
  } catch (error) {
    console.error("üî• Error updating classroom:", error);
    throw error;
  }
};

/** ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏•‡∏ö‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô */
export const deleteClassroom = async (cid, uid) => {
  try {
    await deleteDoc(doc(db, "classroom", cid));

    // üîπ ‡∏•‡∏ö‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô‡∏≠‡∏≠‡∏Å‡∏à‡∏≤‡∏Å `users/{uid}/classroom`
    const userRef = doc(db, "users", uid);
    const userDoc = await getDoc(userRef);

    if (userDoc.exists()) {
      const userData = userDoc.data();
      const updatedClassrooms = { ...userData.classroom };
      delete updatedClassrooms[cid];

      await updateDoc(userRef, { classroom: updatedClassrooms });
    }
  } catch (error) {
    console.error("Error deleting classroom:", error);
    throw error;
  }
};
